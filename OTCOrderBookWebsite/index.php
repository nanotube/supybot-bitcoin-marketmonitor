<h2>Welcome to #bitcoin-otc!</h2>

<p>#bitcoin-otc is an informal place for people to conduct <a href="http://en.wikipedia.org/wiki/Over-the-counter_(finance)">over-the-counter</a> trading in <a href="http://bitcoin.org">bitcoin</a> currency. The marketplace is located in #bitcoin-otc channel on the <a href="http://freenode.net">freenode</a> IRC network.</p>

<h2>Useful resources</h2>

<p>The following resources are available for you:</p>

<ul>
<li><a href="vieworderbook.php">Web view of the open order book</a>.
<li><a href="bitcoinotcguide.php">A guide to using #bitcoin-otc</a>. A must-read for all users: how to use the order book, and how to stay safe while conducting OTC transactions.
</ul>

<h2>Disclaimers</h2>

<p>#bitcoin-otc is merely an aggregator of outstanding supply and demand. All transactions that may occur are conducted directly between counterparties, without any participation or intermediation from #bitcoin-otc. As such, it is each individual's responsibility to conduct due diligence on their counterparties, and otherwise act in a prudent way to avoid falling prey to fraudulent users. It is strongly recommended that all users review the <a href="bitcoinotcguide.php">guide to using #bitcoin-otc</a>, which contains a non-exhaustive list of suggestions for safe conduct on #bitcoin-otc.</p>

